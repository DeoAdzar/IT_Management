package com.example.it_management;

public class SpinnerTypeItem {
    private String mName;
    private int mImage;

    public SpinnerTypeItem(String mName, int mImage) {
        this.mName = mName;
        this.mImage = mImage;
    }

    public String getmName() {
        return mName;
    }

    public int getmImage() {
        return mImage;
    }
}
